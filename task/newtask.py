#1)Write a program that accepts to numbers from the user and calculate: Addition , Subtraction, Division ,Multiplication and floor division

num=int(input("Enter the first Number: "))
num1=int(input("Enter the Second Number: "))

print(f"Addition of {num} and {num1} is",num+num1)
print(f"Subtraction of {num} and {num1} is",num-num1)
print(f"multiplication of {num} and {num1} is",num*num1)
print(f"Division of {num} and {num1} is",num/num1)
print(f"floor divison of {num} and {num1} is",num//num1)