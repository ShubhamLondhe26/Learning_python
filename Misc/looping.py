#iterating over a list,tuple & string or other iterable object
x = ["python","java","Angular","react"]
for i in x:
    print(i) 

print("-"*50)

x = ("python","java","Angular","react")
for i in x:
    print(i) 

print("-"*50)

x = "python"
for i in x:
    print(i) 

print("-"*50)

#Access index of a list using for loop
x = ["python","java","Angular","react"]
for index in range(len(x)):
    value = x[index]
    print(index,value)

print("-"*50)
















